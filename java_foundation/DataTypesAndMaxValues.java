package java_foundation;

public class DataTypesAndMaxValues {

	public static void main(String[] args) {
		byte minByte = Byte.MIN_VALUE;
		byte maxByte = Byte.MAX_VALUE;
		
		System.out.println("Minimum value of Byte: "+ minByte);
		System.out.println("Maximum value of Byte: "+ maxByte);
		
		short minShort = Short.MIN_VALUE;
		short maxShort = Short.MAX_VALUE;
		
		System.out.println("Minimum value of Short: "+ minShort);
		System.out.println("Maximum value of short: "+ maxShort);
		
		int minInt = Integer.MIN_VALUE;
		int maxInt = Integer.MAX_VALUE;
		
		System.out.println("Minimum value of Integer: "+ minInt);
		System.out.println("Maximum value of Integer: "+ maxInt);		
		
		long minLong = Long.MIN_VALUE;
		long maxLong = Long.MAX_VALUE;

		System.out.println("Minimum value of Long: "+ minLong);
		System.out.println("Maximum value of Long: "+ maxLong);	
		
		// float double boolean
		
		float minFloat = Float.MIN_VALUE;
		float maxFloat = Float.MAX_VALUE;

		System.out.println("Minimum value of Float: "+ minFloat);
		System.out.println("Maximum value of Float: "+ maxFloat);	

		double minDouble = Double.MIN_VALUE;
		double maxDouble = Double.MAX_VALUE;

		System.out.println("Minimum value of Double: "+ minDouble);
		System.out.println("Maximum value of Double: "+ maxDouble);	

		boolean minBoolean = Boolean.FALSE;
		boolean maxBoolean = Boolean.TRUE;

		System.out.println("Minimum value of Boolean: "+ minBoolean);
		System.out.println("Maximum value of Boolean: "+ maxBoolean);	

//		Minimum value of Byte: -128
//		Maximum value of Byte: 127
//		Minimum value of Short: -32768
//		Maximum value of short: 32767
//		Minimum value of Integer: -2147483648
//		Maximum value of Integer: 2147483647
//		Minimum value of Long: -9223372036854775808
//		Maximum value of Long: 9223372036854775807
//		Minimum value of Float: 1.4E-45
//		Maximum value of Float: 3.4028235E38
//		Minimum value of Double: 4.9E-324
//		Maximum value of Double: 1.7976931348623157E308
//		Minimum value of Boolean: false
//		Maximum value of Boolean: true
		
	}

}
